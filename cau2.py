import numpy as np
from sklearn.decomposition import PCA # Dùng để so sánh


def run_custom_pca():
    # Cố định seed
    np.random.seed(42)
    
    
    # Input là ma trận 5 x 10
    # N = 5 số mẫu, D = 10 số đặc trưng
    X = np.random.rand(5, 10)
    print(X) # Dữ liệu input
    
    print("-" * 50)
    
    # Bước 1: Chuẩn hóa data
    mean_vector = np.mean(X, axis=0)
    X_centered = X - mean_vector
    
    # Bước 2: Tính ma trận hiệp phương sai
    n_sameples = X.shape[0]
    cov_matrix = np.dot(X_centered.T, X_centered) / (n_sameples - 1)
    print("Ma trận hiệp phương sai")
    print(cov_matrix) # Ma trận hiệp phương sai
    
    print('\n', "-" * 50, "\n")
    
    # Bước 3: Tính eigenvalues và eigenvectors
    eigenvalues, eigenvectors = np.linalg.eigh(cov_matrix)
    
    # Sort giảm dần và update thứ tự
    sorted_indices = np.argsort(eigenvalues)[::-1]
    eigenvalues = eigenvalues[sorted_indices]
    eigenvectors = eigenvectors[:, sorted_indices]
    
    print("Bước 3: Eigenvalues và Eigenvectors sau khi sắp xếp giảm dần")
    
    print("1. Trị riêng (Eigenvalues) đã sắp xếp giảm dần:")
    print(eigenvalues)
    print("\n2. Vector riêng (Eigenvectors) tương ứng:")
    print(eigenvectors)
    print('\n', "-" * 50, "\n")
    
    print(" SO SÁNH VỚI SCIKIT-LEARN PCA CÓ SẴN ")
    pca_sklearn = PCA(n_components=5)
    pca_sklearn.fit(X) 
    
    print("Scikit-learn - Trị riêng (explained_variance_):")
    print(pca_sklearn.explained_variance_)
    
    print("\nScikit-learn - Vector riêng (components_.T):")
    print(pca_sklearn.components_.T)
    
    print('\n', "-" * 50, "\n")
    
    
    # Bước 4: Chọn k thành phần chính
    k_list = [1, 2, 3, 4, 5] 
    
    print("Bước 5: Chiếu dữ liệu xuống không gian mới với các giá trị k khác nhau")
    for k in k_list:
        print(f"\n - GIẢM XUỐNG CÒN k = {k} CHIỀU ")
        # Lấy k cột đầu tiên của ma trận vector riêng để làm ma trận phép chiếu
        projection_matrix = eigenvectors[:, :k]
        
        # Chiếu dữ liệu bằng phép nhân ma trận
        X_reduced = np.dot(X_centered, projection_matrix)
        
        print(f"Kích thước ma trận sau khi chiếu: {X_reduced.shape}")

        # In ma trận sau khi chiếu nếu k = 2
        if k == 2:
            print(X_reduced)
            
    print("-" * 50)
    
    print("VÍ DỤ CHIẾU 1 VECTOR ĐƠN LẺ (Chọn k = 2)")
    k_example = 2
    projection_matrix_example = eigenvectors[:, :k_example]
    
    sample_vector = X[0] # Lấy mẫu dữ liệu đầu tiên (dòng 0)
    print(f"Vector gốc ban đầu (10 chiều): \n{sample_vector}")
    
    sample_centered = sample_vector - mean_vector
    
    # Thực hiện chiếu sang không gian mới
    sample_reduced = np.dot(sample_centered, projection_matrix_example)
    print(f"\nVector sau khi chiếu xuống không gian (k={k_example} chiều): \n{sample_reduced}")

if __name__ == "__main__":
    run_custom_pca()
    
    
    
    
